//D.Fixed Password

#include<bits/stdc++.h>
using namespace std;
int main()
{
    long long n;
    while(1)
    {
        cin>>n;
        if (n!=1999)
        {
            cout<<"Wrong"<<endl;
        }
        else if (n==1999)
        {
            cout<<"Correct"<<endl;
            break;
        }
    }
}

